using UnityEngine;

//////////////////////////////////////////////////////////////////////////////
public class HUD : MonoBehaviour
{
    [Header("References")]
    [SerializeField] private GameObject hud;

    //////////////////////////////////////////////////////////////////////////////
    private void Update()
    {
        //HUD only enable when exploring the level
        hud.SetActive(GameManager.instance.stateOfGame == GameManager.States.InGame);
    }

    //////////////////////////////////////////////////////////////////////////////
}

