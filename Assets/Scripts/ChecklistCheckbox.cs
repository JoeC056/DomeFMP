using UnityEngine;

//////////////////////////////////////////////////////////////////////////////
public class ChecklistCheckbox : MonoBehaviour
{
    [Header("References")]
    private GameObject tickIcon;

    [Header("Parameters")]
    public bool ticked;

    //////////////////////////////////////////////////////////////////////////////
    private void Awake()
    {
        tickIcon = transform.GetChild(0).gameObject;
        tickIcon.SetActive(false);
    }

    //////////////////////////////////////////////////////////////////////////////
    public void OnClick()
    {
        ticked = !ticked;
        tickIcon.SetActive(ticked);
    }

    //////////////////////////////////////////////////////////////////////////////
    public void Untick()
    {
        ticked = false;
    }

    //////////////////////////////////////////////////////////////////////////////
}

//////////////////////////////////////////////////////////////////////////////
