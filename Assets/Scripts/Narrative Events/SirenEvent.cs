using UnityEngine;
using UnityEngine.Events;

//////////////////////////////////////////////////////////////////////////////////
public class SirenEvent : MonoBehaviour
{
    [Header("Parameters")]
    public UnityEvent EventOnCollision;

    //////////////////////////////////////////////////////////////////////////////////
    private void OnTriggerEnter(Collider other)
    {
        if (other.gameObject.CompareTag("Player"))
        {
            Debug.Log("SIREEEEN");
            EventOnCollision.Invoke();
            Destroy(gameObject);
        }
    }

    //////////////////////////////////////////////////////////////////////////////////
}

