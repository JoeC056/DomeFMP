using UnityEngine;

//////////////////////////////////////////////////////////////////////////////
public class SubmitButton : MonoBehaviour
{
    [Header("References")]
    [SerializeField] private GameObject checklist;

    //////////////////////////////////////////////////////////////////////////////
    public void OnClick()
    {
        checklist.GetComponent<Checklist>().CalculateScoreBasedOnAnswers();
        checklist.GetComponent<Checklist>().ResetList();
    }

    //////////////////////////////////////////////////////////////////////////////
}

//////////////////////////////////////////////////////////////////////////////