using UnityEngine;
using UnityEngine.Rendering;
using UnityEngine.Rendering.Universal;

//////////////////////////////////////////////////////////////////////////////
public class CRTFilter : MonoBehaviour
{
    private Volume volume;

    //////////////////////////////////////////////////////////////////////////////
    private void Awake()
    {
        volume = GetComponent<Volume>();
    }

    //////////////////////////////////////////////////////////////////////////////
    private void Update()
    {
        UpdateFilterVisibility();
    }

    //////////////////////////////////////////////////////////////////////////////
    private void UpdateFilterVisibility()
    {
        FilmGrain filmGrainRef = null;

        volume.enabled = SettingsManager.instance.computerCRTFilter;
        volume.profile.TryGet<FilmGrain>(out filmGrainRef);
        filmGrainRef.active = SettingsManager.instance.computerCRTLines;
    }

    //////////////////////////////////////////////////////////////////////////////
}

